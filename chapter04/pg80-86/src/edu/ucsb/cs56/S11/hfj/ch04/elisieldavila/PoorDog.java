class PoorDog{
    private int size;     
    private String name;    //instance variables size and name
    /**
     * A getter to get the size of our "PoorDog" 
     */
    
    public int getSize()
    {
	return size;
    }
    
    /**
     * A getter to get the name of our "PoorDog" 
     */
    
    public String getName()
    {
	return name;
    }
}
